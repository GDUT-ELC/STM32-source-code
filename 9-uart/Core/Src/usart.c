/* USER CODE BEGIN 1 */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if (huart->Instance == UART5)
	{
		uart_rx_buf[rx_point++] = rx_data;
		HAL_UART_Receive_IT(&huart5, &rx_data, 1);
	}
}

void uart_rx_callback(void)
{
	if(rx_point > 0)
	{
		HAL_UART_Transmit(&huart5, (uint8_t*)uart_rx_buf, strlen(uart_rx_buf), HAL_MAX_DELAY);
	}
	rx_point = 0;
	
	memset(uart_rx_buf, 0, UART_RX_LEN);
}
